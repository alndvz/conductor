export function formatFileChangeNotification(file: string, commit: string, diff: string): string {
  const codeFence = "```"
  return `File ${file} changed (commit ${commit.slice(0, 7)})\n\n${codeFence}diff\n${diff}\n${codeFence}`
}

export function formatAgentSwitchNotification(agent: string): string {
  return `Agent switched to ${agent}`
}
